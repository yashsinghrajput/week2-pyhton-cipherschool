# "+" for addition
# "-" for substraction
# "*" for multipliaction
# "/" for float division, it gives value in decimal
# "//" for integer division, it gives value only in integer
# "%" it is Modulo give remainder
# "**" for exponent
print(2+3)
print(5-2)
print(3*2)
print(5/2)
print(5//2)
print(5%2)
print(5**2)
print(round(2**0.5, 4))